<script setup lang="ts">
import { Link } from "@inertiajs/vue3";

interface Link {
  active: boolean;
  label: string;
  url: string;
}
interface Props {
  links: Array<Link>;
}
defineProps<Props>();
</script>
<template>
  <div v-if="links.length > 3" class="m-auto mt-10">
    <div class="flex flex-wrap mb-1">
      <template v-for="(link, index) in links" :key="index">
        <div
          v-if="link.url === null"
          class="mr-1 mb-1 px-4 py-3 text-sm leading-4 text-gray-400 border rounded"
          v-html="link.label"
        />
        <Link
          v-else
          class="mr-1 px-4 py-3 text-sm leading-4 border rounded focus:border-indigo-500 focus:text-indigo-500"
          :class="{
            'bg-indigo-500 text-white': link.active,
            'hover:bg-indigo-100': !link.active,
          }"
          :href="link.url"
          v-html="link.label"
        />
      </template>
    </div>
  </div>
</template>
